let angka = 0;

console.log("ini looping maju");
//loping maju
while (angka < 10) {
  console.log(angka);
  angka++;
}

console.log("ini looping mundur");
//looping mundur
while (angka > 0) {
  console.log(angka);
  angka--;
}

